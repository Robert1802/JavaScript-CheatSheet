// Variables

/*  Variables in JavaScript are simply names of storage locations.
    In other words, they can be considered as stand-in values that we can use to perform various operations in our JavaScript codes.
    JavaScript allows the usage of variables in the following three ways: */

// Var
var x = 140; //  variable x can be reassigned a new value and also redeclared

// Let
let x = 202; // variable x cannot be redeclared but can be reassigned a new value

// Const 
const x = 5; // variable x cannot be reassigned a new value or redeclared